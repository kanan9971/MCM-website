  /* ── Apply language to page ── */
  function applyLang(lang) {
    var els = document.querySelectorAll('[data-i18n]');
    if (!translations[lang]) return;

    els.forEach(function (el) {
      var key = el.getAttribute('data-i18n');
      var text = translations[lang][key];
      if (text !== undefined) {
        el.innerHTML = text;
      }
    });

    /* Update lang-switch button active state */
    var btns = document.querySelectorAll('.lang-switch .lang-option');
    btns.forEach(function (btn) {
      var isEn = btn.textContent.trim() === 'EN';
      if (lang === 'en' && isEn) {
        btn.classList.add('is-active');
      } else if (lang === 'zh' && !isEn) {
        btn.classList.add('is-active');
      } else {
        btn.classList.remove('is-active');
      }
    });

    localStorage.setItem('mcm-lang', lang);
  }

  /* ── Switch language (called from button onclick) ── */
  window.switchLang = function (lang) {
    applyLang(lang);
  };

  /* ── Init on DOM ready ── */
  function init() {
    var saved = localStorage.getItem('mcm-lang') || 'en';
    applyLang(saved);
  }

  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', init);
  } else {
    init();
  }
